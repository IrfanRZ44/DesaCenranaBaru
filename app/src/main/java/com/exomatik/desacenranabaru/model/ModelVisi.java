package com.exomatik.desacenranabaru.model;

public class ModelVisi {
    String Visi, Misi;

    public ModelVisi() {
    }

    public ModelVisi(String visi, String misi) {
        Visi = visi;
        Misi = misi;
    }

    public String getVisi() {
        return Visi;
    }

    public void setVisi(String visi) {
        Visi = visi;
    }

    public String getMisi() {
        return Misi;
    }

    public void setMisi(String misi) {
        Misi = misi;
    }
}
